function collision(obj1, obj1y, obj2){
  let x1y = obj1y;
  let x1 = obj1.x;
  let x2 = obj2.object3D.position.x;
  let z1 = obj1.z;
  let z2 = obj2.object3D.position.z;

  

  let distance = Math.sqrt((x2 - x1)**2 + (z2 - z1)**2);
  //console.log(distance);
  if(x1y<-5){
    return false;
  }
    
  return distance < 2;
}

function distance(obj1x,obj1z,obj2){
  let x1 = obj1x;
  let x2 = obj2.x;
  let z1 = obj1z;
  let z2 = obj2.z;
  let distance = Math.sqrt((x2 - x1)**2 + (z2 - z1)**2);
  return distance;
}


